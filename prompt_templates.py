def get_prompt(category: str, content: str) -> str:
    templates = {
        "시정홍보": f"""
당신은 충주시청의 홍보 담당입니다. 아래 시정 안내문을 바탕으로 카카오채널에 게시할 메시지를 작성해 주세요.

- 공공기관의 품위는 유지하면서도 시민이 이해하기 쉽게  
- 놀러오세요! 같은 표현은 피하고, 공공 알림 톤으로  
- 제목 1줄, 본문 4~6줄  
- 기간, 장소, 방법 등은 이모지로 항목 구분  
- 말투는 단정하고 공손하게

[시정홍보 내용]
{content}
""",

        "정책공지": f"""
충주시청의 공식 정책 안내 메시지를 작성해 주세요.

- 개편사항, 대상, 혜택 등 핵심을 쉽게 전달  
- 제목 1줄, 본문 4~5줄  
- 말투는 설명하듯 자연스럽고 행정적 표현은 쉽게 풀어쓸 것  
- 이모지로 항목을 정리하고, 친근함보다는 정확성 중심

[정책공지 내용]
{content}
""",

        "축제": f"""
충주시가 주최하는 축제를 홍보하는 카카오채널 메시지를 작성해 주세요.

- 시민이 참여하고 싶어지도록 활기차고 초대하는 느낌으로  
- 제목 1줄, 본문 4~6줄  
- 일정, 장소, 프로그램을 이모지로 항목 정리  
- 말투는 밝고 유쾌하게, 공공기관 톤을 유지하면서도 부담 없이

[축제 정보]
{content}
""",

        "이벤트": f"""
다음은 충주시 또는 소속 기관에서 주최하는 시민 참여 이벤트입니다.

- 참여 방법, 혜택, 기한 등 안내 중심  
- 제목 1줄, 본문은 후킹 + 핵심 정보 정리  
- 이모지로 항목 구분, 말투는 친근하고 쉽게  
- 너무 캐주얼하지 않도록 적절한 균형 유지

[이벤트 내용]
{content}
""",

        "재난알림": f"""
충주시민에게 전달되는 긴급 재난/안전 알림 메시지입니다.

- 전달력과 명확성이 최우선  
- 제목 1줄, 본문은 3~5줄 이내  
- 주의사항은 강조, 말투는 냉정하고 침착하게  
- 이모지는 제한적 사용 (위치, 시간 등만)

[재난알림 내용]
{content}
""",

        "기타": f"""
아래 내용을 바탕으로 시민에게 유용한 정보를 제공하는 카카오채널 메시지를 작성해 주세요.

- 제목은 1줄, 본문은 4~6줄로 구성  
- 말투는 정중하면서도 이해하기 쉽게  
- 주요 항목(기간, 장소, 대상 등)은 이모지로 정리  
- 공공기관 톤을 유지하면서 시민 눈높이에 맞춘 서술

[기타 안내 내용]
{content}
""",
        "문화행사": f"""
다음은 충주시에서 시민을 대상으로 진행하는 문화행사 안내입니다.

- 공연, 전시, 강연, 체험 등 문화적 가치 중심으로 전달  
- 말투는 부드럽고 세련되게, 공공기관 품위 유지  
- 제목은 간결하게 1줄, 본문은 4~6줄 구성  
- 일정, 장소, 관람방법 등은 이모지로 항목 구분  
- 유아·청소년·가족 등 대상이 있다면 명확히 표기

[문화행사 내용]
{content}
"""

    }

    return templates.get(category, f"[카테고리: {category}]\n{content}")

