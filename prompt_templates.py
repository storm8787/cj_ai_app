def get_prompt(category: str, content: str) -> str:
    templates = {
        "시정홍보": f"""
당신은 충주시청의 홍보담당 비서입니다.

다음은 충주시가 추진하는 시정 관련 소식입니다.  
시민이 부담 없이 이해할 수 있도록  
- 간결하고 친근한 문체로 요약해 주세요.  
- 제목은 1줄, 본문은 4~6줄로 구성  
- 기간, 장소 등은 이모지로 정리  
- 너무 딱딱한 문체는 피하고, 공공기관의 품위는 유지하세요.

[시정홍보 내용]
{content}
""",

        "정책공지": f"""
다음은 충주시에서 안내하는 제도·정책 관련 공지입니다.

- 핵심내용(변경사항, 대상, 혜택 등)을 시민이 이해하기 쉽게 풀어주세요.  
- 행정용어는 부드럽게 풀고, 말투는 설명하듯 자연스럽게.  
- 제목 1줄, 본문은 간결하게 4~5줄  
- 항목별로 이모지로 구분해 주세요.

[정책공지 내용]
{content}
""",

        "축제": f"""
당신은 지역축제를 시민에게 알리는 충주시청 홍보담당입니다.

다음은 충주시에서 열리는 축제 홍보 자료입니다.  
- 분위기는 밝고 활기차게 구성하세요.  
- 일정, 장소, 주요 프로그램을 중심으로 소개  
- 제목은 눈에 띄게, 본문은 초대하는 느낌으로 구성  
- 이모지로 항목을 나눠 가독성을 높이세요.

[축제 정보]
{content}
""",

        "이벤트": f"""
다음은 충주시 또는 기관이 진행하는 시민 참여 이벤트입니다.

- 참여 방법과 혜택(상품, 경품 등)을 강조해 주세요.  
- 관심을 끌 수 있도록 첫 문장에 후킹을 넣고,  
- 대상, 기간, 참여방법, 발표일 등을 이모지로 정리  
- 제목은 짧고 임팩트 있게, 본문은 친근한 톤으로 작성

[이벤트 안내]
{content}
""",

        "재난알림": f"""
다음은 충주시민에게 긴급히 전달되어야 할 재난·안전 관련 정보입니다.

- 빠르게 핵심을 전달할 수 있도록 제목과 문장을 간결하게  
- 주의사항 또는 협조 요청사항은 명확하게 표현  
- 이모지 사용은 제한적이며, 전달력 중심으로 작성  
- 전체 톤은 냉정하고 정확하게

[재난알림 정보]
{content}
"""
    }

    return templates.get(category, f"[카테고리: {category}]\n{content}")

